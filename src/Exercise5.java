public class Exercise5 {
    public static void main(String[] args) {
        Fibonacci();
    }

    static void Fibonacci() {
        int broj1 = 0;
        int broj2 = 1;
        int sum = 0;
        while (true) {
            for (int i = 0; i <= 20; i++) {
                sum = sum + broj1 + broj2;
                sum = broj2;

            }
        }
    }
}
